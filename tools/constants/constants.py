def require_mpmath():
    """Fail clearly if mpmath is unavailable."""
    if not MPMATH_AVAILABLE:
        raise RuntimeError(
            "mpmath is required for digit generation.\n"
            "Install with: pip install mpmath"
        )


def get_constant_meta(name):
    """Return (key, meta) for a constant name, or raise ValueError."""
    key = name.lower()
    if key not in CONSTANTS:
        raise ValueError("unknown constant: {0}".format(name))
    return key, CONSTANTS[key]


def get_digits(name, count):
    """
    Return the first `count` decimal digits of the named constant as a plain
    digit string (no decimal point, no whitespace).

    Digits begin with the leading integer digit, e.g. pi -> '31415...'.
    mpmath global precision is saved and restored.
    """
    require_mpmath()
    key, meta = get_constant_meta(name)

    old_dps = mpmath.mp.dps
    try:
        mpmath.mp.dps = count + EXTRA_PRECISION
        value = meta["fn"](mpmath.mp)
        rendered = mpmath.nstr(value, count + 20, strip_zeros=False)
        digits = "".join(ch for ch in rendered if ch.isdigit())
    finally:
        mpmath.mp.dps = old_dps

    if len(digits) < count:
        raise RuntimeError(
            "insufficient digits generated for {0}: wanted {1}, got {2}".format(
                key, count, len(digits)
            )
        )

    return digits[:count]


def format_digits(digits, group=GROUP_SIZE, per_line=GROUPS_PER_LINE):
    """
    Format a digit string into grouped, line-wrapped output.

    Default: 10 digits per group, 6 groups per line (60 digits per line).
    """
    groups = [digits[i:i + group] for i in range(0, len(digits), group)]
    lines = [
        " ".join(groups[i:i + per_line])
        for i in range(0, len(groups), per_line)
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Hashing and integrity (CAHF §4)
# ---------------------------------------------------------------------------

def _normalise_line_endings(text):
    """
    Normalise line endings to LF per CAHF §4.1.
    CRLF must be replaced before bare CR to avoid double-replacement.
    """
    text = text.replace("\r\n", "\n")
    text = text.replace("\r", "\n")
    return text


def _normalise_body(body):
    """
    Normalise line endings and strip trailing newlines per CAHF §4.1-4.2.
    This produces the string that is hashed during stamping and verification.
    """
    return _normalise_line_endings(body).rstrip("\n")


def sha256_hex(text):
    """
    Return SHA-256 of a Unicode string encoded as UTF-8.
    Input must already be normalised per CAHF §4.3.
    """
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def crc32_hex(text):
    """
    Return CRC32 of a Unicode string encoded as UTF-8, as an 8-character
    lowercase hex string.  Provides a quick manual/transcription error check.
    """
    data = text.encode("utf-8")
    return "{0:08x}".format(zlib.crc32(data) & 0xffffffff)


# ---------------------------------------------------------------------------
# CAHF file generation (draft-darley-crowsong-archivist-00 §5)
# ---------------------------------------------------------------------------

def make_cahf_file(name, count, digits):
    """
    Build and return a complete CAHF-stamped constant digit file as a
    Unicode string, ready to write to disk.

    SHA256 and CRC32 are computed over the normalised formatted body
    (i.e. the digit string with spaces and newlines), NOT over the raw
    digit string.  This ensures that any CAHF-conformant verifier can
    recompute the digest by extracting the body after the sentinel and
    applying the standard CAHF hashing contract (§4.4).

    File structure (per CAHF §5):
        <header block>
        # ---END-HEADER---
        <body>
        <one trailing LF>
    """
    _, meta = get_constant_meta(name)
    date = time.strftime("%Y-%m-%d")

    # The body is the formatted digit string.
    # Normalise it before hashing per CAHF §4.4(a).
    body_raw = format_digits(digits)
    body_for_hash = _normalise_body(body_raw)
    digest = sha256_hex(body_for_hash)
    crc    = crc32_hex(body_for_hash)

    header_lines = [
        CAHF_MARKER,
        "#",
        "# TITLE      {0} — {1}".format(meta["symbol"], meta["name"]),
        "# CONSTANT   {0}".format(name),
        "# OEIS       {0}".format(meta["oeis"]),
        "# NOTE       {0}".format(meta["notes"]),
        "#",
        "# DIGITS     {0}".format(count),
        "# FORMAT     decimal, {0} digits per group, {1} groups per line".format(
            GROUP_SIZE, GROUPS_PER_LINE
        ),
        "#",
        "# NOTE       Named constants are public deterministic reference material.",
        "# NOTE       They provide no secrecy, unpredictability, or cryptographic",
        "# NOTE       strength.  Suitable as declared IV anchors only.",
        "#",
        "# DATE       {0}".format(date),
        "# TOOL       {0} (Proper Tools SRL)".format(TOOL_PATH),
        "#",
        "# CRC32      {0}".format(crc),
        "# SHA256     {0}".format(digest),
    ]

    header_block = "\n".join(header_lines)

    # CAHF §5 step 5: header + sentinel + body + one trailing LF.
    # No blank line between sentinel and body (§2.3).
    return (
        header_block + "\n" +
        CAHF_SENTINEL + "\n" +
        body_for_hash + "\n"
    )


# ---------------------------------------------------------------------------
# CAHF file parsing (draft-darley-crowsong-archivist-00 §6)
# ---------------------------------------------------------------------------

def parse_cahf_file(path):
    """
    Parse a CAHF-stamped constant digit file.

    Implements the verification parsing procedure from CAHF §6, including:
      - ARCHIVIST marker gate (§6.1 step 3)
      - Sentinel detection (§6.1 step 4)
      - Body extraction and normalisation (§6.2, §4.4(c))
      - Strict body character validation

    Returns a dict with keys:
        fields          -- dict of all parsed header fields (uppercase keys)
        body_for_hash   -- normalised body string (what to hash)
        digits          -- digit characters extracted from body
        sha256_declared -- value of SHA256 header field, or None
        name_declared   -- value of CONSTANT header field, or None
        count_declared  -- value of DIGITS header field as int, or None
        crc32_declared  -- value of CRC32 header field, or None

    Raises ValueError with a descriptive message for any structural problem.
    """
    with io.open(path, "r", encoding="utf-8") as f:
        content = f.read()

    # Normalise line endings per CAHF §4.1
    content = _normalise_line_endings(content)
    lines = content.split("\n")

    # Gate on ARCHIVIST marker per CAHF §6.1 step 3.
    # Require exact match against CAHF_MARKER so version mismatches are caught
    # explicitly rather than silently accepted.
    first_nonempty = next((l for l in lines if l.strip()), "")
    if first_nonempty != CAHF_MARKER:
        raise ValueError(
            "not a CAHF file or unsupported version: "
            "expected {0!r} as first non-empty line, got {1!r}.\n"
            "Re-generate this file with a CAHF-conformant tool.".format(
                CAHF_MARKER, first_nonempty
            )
        )

    # Locate sentinel per CAHF §6.1 step 4
    if CAHF_SENTINEL not in lines:
        raise ValueError(
            "legacy or malformed CAHF file: sentinel '{0}' not found.\n"
            "Re-stamp this file with a CAHF-conformant tool.".format(CAHF_SENTINEL)
        )

    sentinel_idx = lines.index(CAHF_SENTINEL)
    header_lines = lines[:sentinel_idx]
    body_lines   = lines[sentinel_idx + 1:]

    # Parse header fields per CAHF §2.2
    # Accept both canonical double-space and legacy colon separator (§7.3),
    # but only for the known CAHF field names.
    #
    # Singleton fields must appear at most once; duplicates in integrity-critical
    # fields (SHA256, CRC32, CONSTANT, DIGITS) indicate a malformed or tampered
    # header and are rejected immediately.  Repeatable fields (NOTE) accumulate
    # as lists.  Unknown fields are stored as singletons (last-write-wins).
    SINGLETON_FIELDS = {
        "ARCHIVIST", "SHA256", "CRC32", "TITLE", "AUTHOR", "LANG",
        "SOURCE", "DATE", "FETCHED", "ENTRIES", "CHARS", "LINES",
        "TAGS", "TLP", "LICENSE", "TOOL",
        # Tool-specific extensions
        "CONSTANT", "OEIS", "DIGITS", "FORMAT",
    }
    REPEATABLE_FIELDS = {"NOTE"}
    # Combined set for the field-recognition gate
    KNOWN_FIELDS = SINGLETON_FIELDS | REPEATABLE_FIELDS

    fields = {}

    def _store_field(fname, fval):
        """Store a parsed header field.

        Singleton fields raise ValueError on duplication.
        Repeatable fields (NOTE) accumulate as lists.
        Unknown fields are stored as singletons by default.
        """
        if fname in SINGLETON_FIELDS:
            if fname in fields:
                raise ValueError(
                    "duplicate singleton header field: {0!r} — "
                    "file is malformed or tampered.".format(fname)
                )
            fields[fname] = fval
            return
        if fname in REPEATABLE_FIELDS:
            if fname in fields:
                existing = fields[fname]
                if isinstance(existing, list):
                    existing.append(fval)
                else:
                    fields[fname] = [existing, fval]
            else:
                fields[fname] = fval
            return
        # Unknown field — store as singleton (last-write-wins is fine for unknowns)
        fields[fname] = fval

    for line in header_lines:
        if not line.startswith("# "):
            continue
        rest = line[2:]
        if not rest or rest == "#":
            continue

        # Try canonical whitespace-separated format first (§2.2).
        # We accept any run of whitespace as separator for robustness across
        # tools, and document this as the verified-acceptable form.
        parts = rest.split(None, 1)
        if len(parts) == 2 and parts[0].upper() in KNOWN_FIELDS:
            _store_field(parts[0].upper(), parts[1].strip())
        elif len(parts) == 1 and parts[0].upper().rstrip(":") in KNOWN_FIELDS:
            # bare field name with no value
            _store_field(parts[0].upper().rstrip(":"), "")
        else:
            # Try legacy colon separator (§7.3), restricted to known fields
            if ":" in rest:
                fname, _, fval = rest.partition(":")
                fname = fname.strip().upper()
                if fname in KNOWN_FIELDS:
                    _store_field(fname, fval.strip())

    # Extract and validate the body per CAHF §6.2 and §4.4(c).
    # Use _normalise_body() for exact symmetry with the stamping path.
    body_text = "\n".join(body_lines)
    body_for_hash = _normalise_body(body_text)

    # Strict body character validation: only digits and whitespace permitted.
    # Any other character indicates tampering or format corruption.
    for lineno, bline in enumerate(body_for_hash.split("\n"), 1):
        for ch in bline:
            if not (ch.isdigit() or ch in " \t"):
                raise ValueError(
                    "invalid character {0!r} in digit body at line {1}.\n"
                    "Expected only decimal digits and whitespace.".format(ch, lineno)
                )

    digits = "".join(ch for ch in body_for_hash if ch.isdigit())

    # Parse typed fields
    sha256_declared = fields.get("SHA256")
    crc32_declared  = fields.get("CRC32")
    name_declared   = fields.get("CONSTANT")

    count_declared = None
    if "DIGITS" in fields:
        try:
            count_declared = int(fields["DIGITS"])
        except ValueError:
            raise ValueError(
                "malformed DIGITS field: {0!r}".format(fields["DIGITS"])
            )

    return {
        "fields":          fields,
        "body_for_hash":   body_for_hash,
        "digits":          digits,
        "sha256_declared": sha256_declared,
        "crc32_declared":  crc32_declared,
        "name_declared":   name_declared,
        "count_declared":  count_declared,
    }


