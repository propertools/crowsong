def positive_int(value):
    """argparse type: integer >= 1."""
    try:
        ivalue = int(value)
    except ValueError:
        raise argparse.ArgumentTypeError("must be an integer >= 1")
    if ivalue < 1:
        raise argparse.ArgumentTypeError("must be >= 1")
    return ivalue


