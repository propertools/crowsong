def cmd_list(_args):
    """List all known constants."""
    fmt = "{:<8}  {:<6}  {:<10}  {}"
    out(fmt.format("name", "symbol", "OEIS", "notes"))
    out("-" * 72)
    for key in sorted(CONSTANTS):
        meta = CONSTANTS[key]
        out(fmt.format(
            key,
            meta["symbol"],
            meta["oeis"],
            meta["notes"][:50]
        ))
    return 0


def cmd_digits(args):
    """Print plain digits only — pipeable."""
    out(get_digits(args.name, args.count))
    return 0


def cmd_show(args):
    """Print formatted digits with heading."""
    key, meta = get_constant_meta(args.name)
    digits = get_digits(key, args.count)
    out("{symbol} — {name} ({count} digits)".format(
        symbol=meta["symbol"],
        name=meta["name"],
        count=args.count
    ))
    out()
    out(format_digits(digits))
    return 0


def cmd_generate(args):
    """Generate a CAHF-stamped constant digit file."""
    key, _meta = get_constant_meta(args.name)
    digits = get_digits(key, args.count)
    file_content = make_cahf_file(key, args.count, digits)

    outdir = os.path.dirname(args.outfile)
    if outdir:
        try:
            os.makedirs(outdir)
        except OSError:
            if not os.path.isdir(outdir):
                raise

    with io.open(args.outfile, "w", encoding="utf-8") as f:
        f.write(file_content)

    # Recompute for display — body_for_hash is format_digits(digits).rstrip("\n")
    body_for_hash = _normalise_body(format_digits(digits))
    out("Written:  {0}".format(args.outfile))
    out("Digits:   {0}".format(args.count))
    out("CRC32:    {0}".format(crc32_hex(body_for_hash)))
    out("SHA256:   {0}".format(sha256_hex(body_for_hash)))
    return 0


def cmd_verify(args):
    """
    Verify a CAHF-stamped constant digit file.

    Checks:
      1. CAHF structural validity (ARCHIVIST marker, sentinel)
      2. All required metadata fields present (CONSTANT, DIGITS, CRC32, SHA256)
      3. Declared CONSTANT matches the name given on the CLI
      4. Declared DIGITS matches the digit count found in the body
      5. CRC32 of body matches declared CRC32
      6. SHA256 of body matches declared SHA256
      7. Digits recomputed from first principles match the file body

    Any single failure causes overall FAIL and exit code 1.
    CRC32 is mandatory: files lacking it must be re-generated.
    """
    key, _meta = get_constant_meta(args.name)

    parsed = parse_cahf_file(args.infile)

    digits        = parsed["digits"]
    body_for_hash = parsed["body_for_hash"]

    if not digits:
        err("Error: no digits found in file body.")
        return 1

    # --- Require all mandatory fields ---
    # CRC32 is mandatory: this tool always emits it, so its absence means
    # the file was not generated by a conformant tool and should be re-generated.
    missing = []
    if parsed["name_declared"] is None:
        missing.append("CONSTANT")
    if parsed["count_declared"] is None:
        missing.append("DIGITS")
    if parsed["sha256_declared"] is None:
        missing.append("SHA256")
    if parsed["crc32_declared"] is None:
        missing.append("CRC32")
    if missing:
        err("Error: missing required header fields: {0}".format(", ".join(missing)))
        err("Re-generate this file with: python constants.py generate <name> <count> <outfile>")
        err("Verification: FAIL")
        return 1

    # --- Run all checks ---
    count = len(digits)

    name_ok  = (parsed["name_declared"].lower() == key)
    count_ok = (parsed["count_declared"] == count)

    sha256_actual = sha256_hex(body_for_hash)
    sha256_ok     = (sha256_actual == parsed["sha256_declared"].lower())

    crc32_actual  = crc32_hex(body_for_hash)
    crc32_ok      = (crc32_actual == parsed["crc32_declared"].lower())

    recomputed = get_digits(key, count)
    digits_ok  = (digits == recomputed)

    # --- Report ---
    def status(ok, extra=""):
        return "OK" if ok else "FAIL" + (" ({0})".format(extra) if extra else "")

    out("File:         {0}".format(args.infile))
    out("Constant:     {0} — {1}".format(
        key,
        status(name_ok, "declared: {0}".format(parsed["name_declared"]))
    ))
    out("Digits:       {0} — {1}".format(
        count,
        status(count_ok, "declared: {0}".format(parsed["count_declared"]))
    ))
    out("CRC32:        {0} — {1}".format(
        crc32_actual,
        status(crc32_ok, "declared: {0}".format(parsed["crc32_declared"]))
    ))
    out("SHA256:       {0} — {1}".format(
        sha256_actual,
        status(sha256_ok, "declared: {0}".format(parsed["sha256_declared"]))
    ))
    out("Recomputed:   {0}".format(
        "OK — digits match" if digits_ok else "FAIL — digits differ"
    ))

    all_ok = name_ok and count_ok and sha256_ok and crc32_ok and digits_ok
    out("Verification: {0}".format("PASS" if all_ok else "FAIL"))
    return 0 if all_ok else 1


