"""
constants.py — named mathematical constant digit generator

Generates digit strings for named constants suitable for use as
initialisation vectors in the Channel Camouflage Layer and Mnemonic
Share Wrapping constructions.

Generated files conform to the Crowsong Archivist Header Format (CAHF)
as specified in draft-darley-crowsong-archivist-00.  Any CAHF-conformant
tool (archivist.py, texts.py, udhr.py, etc.) can verify the output of
this tool, and this tool can verify any CAHF-stamped file that carries
CONSTANT and DIGITS fields.

Usage:
    python constants.py list
    python constants.py digits <name> <count>
    python constants.py show <name> <count>
    python constants.py generate <name> <count> <outfile>
    python constants.py verify <name> <infile>

Examples:
    python constants.py list
    python constants.py digits pi 100
    python constants.py show phi 200
    python constants.py generate sqrt2 10000 docs/constants/sqrt2-10000.txt
    python constants.py verify pi docs/constants/pi-10000.txt

Requires:
    mpmath  (pip install mpmath)

Generated files are plain UTF-8 text and require no dependencies to read.

CAHF conformance: draft-darley-crowsong-archivist-00, sections 2-6, 8.
SHA256 and CRC32 are computed over the normalised formatted body (spaces
and line-breaks included), not over the raw digit string.  Both fields
are mandatory: files generated by older tools that lack CRC32 must be
re-generated before they will pass verification.

Named constants are public deterministic reference material.  They are
suitable as declared IV anchors but provide no secrecy, unpredictability,
or cryptographic strength.

Compatibility: Python 2.7+ / 3.x
Author: Proper Tools SRL
License: MIT
"""

